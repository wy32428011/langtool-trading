import itertools

def get_all_combinations(input_list):
    """
    计算输入数组的所有两两组合（不重复）
    :param input_list: 输入数组
    :return: 组合列表
    """
    # 使用 itertools.combinations 生成不重复的组合
    # 第二个参数 2 表示两两组合
    combinations = list(itertools.combinations(input_list, 2))
    return combinations

def main():
    print("请输入一组元素，用空格、逗号或分号分隔:")
    user_input = input("输入: ").strip()
    
    if not user_input:
        print("输入为空，退出。")
        return

    # 简单的分隔符处理
    import re
    # 按照分号、逗号或空格分隔
    elements = re.split(r'[;,\s]+', user_input)
    # 过滤掉空字符串
    elements = [e.strip() for e in elements if e.strip()]
    
    if len(elements) < 2:
        print(f"输入的元素太少 ({len(elements)} 个)，无法进行两两组合。")
        return

    print(f"正在为以下元素计算组合: {elements}")
    
    results = get_all_combinations(elements)
    
    print(f"\n找到 {len(results)} 组不重复组合:")
    for i, (a, b) in enumerate(results, 1):
        print(f"{i}. ({a}, {b})")

